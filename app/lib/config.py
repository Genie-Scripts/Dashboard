# 営業平日判定
# 「平日」= 土日・国民の祝日（jpholidayによる動的取得）・
#           年末年始（12/29〜1/3）を除いた日
# ──────────────────────────────
import jpholiday as _jpholiday


def is_operational_day(dt) -> bool:
    """
    病院の営業平日かどうかを返す。

    除外条件:
      - 土曜・日曜（weekday >= 5）
      - 国民の祝日・振替休日（jpholiday で動的判定）
      - 年末年始（12/29〜12/31、1/1〜1/3）

    jpholiday は内部で毎年の祝日を法律に基づいて計算するため、
    成人の日・敬老の日等の第N月曜日ルールや振替休日も自動対応。
    年度ごとのマスタ更新は不要。
    """
    import pandas as _pd
    ts = _pd.Timestamp(dt)
    # 土日
    if ts.weekday() >= 5:
        return False
    # 年末年始
    if ts.month == 12 and ts.day >= 29:
        return False
    if ts.month == 1 and ts.day <= 3:
        return False
    # 国民の祝日・振替休日
    if _jpholiday.is_holiday(ts.date()):
        return False
    return True

